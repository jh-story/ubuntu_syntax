# 현재 위치 경로를 출력
pwd

# 현재 위치에서 파일 및 폴더 조회
ls

# 목록 조회 자세히
ls -l

# 목록 조회 자세히, 숨김 파일까지
ls -al

# 목록 조회 자세히, 숨김 파일, 오래된순으로 정렬 (최신순 정렬은 -alt)
ls -alrt

